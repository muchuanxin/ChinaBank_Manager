package com.xd.aselab.chinabankmanager.activity.Manager.model;

public class LobbyVO {

    private String lobby_account;
    private String lobby_name;
    private String lobby_tel;
    private String lobby_siji_num;
    private String lobby_siji_name;
    private int lobby_recommend_num;
    private int lobby_success_num;

    public String getLobby_account() { return lobby_account;}
    public void setLobby_account(String lobby_account) { this.lobby_account = lobby_account;}

    public String getLobby_name() { return lobby_name;}
    public void setLobby_name(String lobby_name) { this.lobby_name = lobby_name;}

    public String getLobby_tel() { return lobby_tel;}
    public void setLobby_tel(String lobby_tel) { this.lobby_tel = lobby_tel;}

    public String getLobby_siji_num() { return lobby_siji_num;}
    public void setLobby_siji_num(String lobby_siji_num) { this.lobby_siji_num = lobby_siji_num;}

    public String getLobby_siji_name() { return lobby_siji_name;}
    public void setLobby_siji_name(String lobby_siji_name) { this.lobby_siji_name = lobby_siji_name;}

    public int getLobby_recommend_num() { return lobby_recommend_num;}
    public void setLobby_recommend_num(int lobby_recommend_num) { this.lobby_recommend_num = lobby_recommend_num;}

    public int getLobby_success_num() { return lobby_success_num;}
    public void setLobby_success_num(int lobby_success_num) { this.lobby_success_num = lobby_success_num;}

}
