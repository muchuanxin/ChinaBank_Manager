package com.xd.aselab.chinabankmanager.kafenqi;

public class NonCarVO {

    private String applicant_id;
    private String applicant_name;
    private String time;
    private double total_money;
    private String tel;
    private int installment_num;
    private String serial_num;
    private double loan_money;
    private String product_type;
    private String confirm;

    public String getApplicant_id() { return applicant_id; }
    public void setApplicant_id(String applicant_id) { this.applicant_id = applicant_id; }


    public String getApplicant_name() { return applicant_name; }
    public void setApplicant_name(String applicant_name) { this.applicant_name = applicant_name; }

    public String getTime() { return time; }
    public void setTime(String time) { this.time = time; }

    public double getTotal_money() { return total_money; }
    public void setTotal_money(double total_money) { this.total_money = total_money; }

    public String getTel() { return tel; }
    public void setTel(String tel) { this.tel = tel; }

    public int getInstallment_num() { return installment_num; }
    public void setInstallment_num(int installment_num) { this.installment_num = installment_num; }

    public String getSerial_num() { return serial_num; }
    public void setSerial_num(String serial_num) { this.serial_num = serial_num; }

    public String getProduct_type() { return product_type; }
    public void setProduct_type(String product_type) { this.product_type = product_type; }

    public double getLoan_money() { return loan_money; }
    public void setLoan_money(double loan_money) { this.loan_money = loan_money; }

    public String getConfirm() { return confirm; }
    public void setConfirm(String confirm) { this.confirm = confirm; }

}
